public class Principal {

    public static void main(String[] args) {
        
        //Variável
        int soma = 0;
        
        //Entrada
        System.out.println("A soma dos 100 primeiros números é: ");
        
        //Processamento
        for (int i = 1; i<=100; i++){
            soma +=i;
        }
        System.out.println(soma);
                  
    }
    
}
